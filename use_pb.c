#include "push_swap.h"

void	use_pb(t_stock **a, t_stock **b)
{
	t_stock	*save;

	if (*a)
	{
		save = 0;
		if ((*a)->next)
			save = (*a)->next;
		(*a)->next = *b;
		*b = *a;
		*a = save;
	}
}
